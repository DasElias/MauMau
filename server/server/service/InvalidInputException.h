#pragma once
#include <stdexcept>

namespace card {
	class InvalidInputException : public std::runtime_error {
		// ----------------------------------------------------------------------
		// --------------------------------FIELDS--------------------------------
		// ----------------------------------------------------------------------
		private:
			int statusCode;

		// ----------------------------------------------------------------------
		// -----------------------------CONSTRUCTORS-----------------------------
		// ----------------------------------------------------------------------
		public:
			InvalidInputException(int statusCode) throw();
			
		// ----------------------------------------------------------------------
		// -------------------------------METHODS--------------------------------
		// ----------------------------------------------------------------------
		public:	
			virtual char const* what() const throw();
			int getStatusCode() const throw();
	};
}